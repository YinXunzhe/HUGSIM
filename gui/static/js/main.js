function quaternionToMatrix(quaternion) {
    const r = quaternion[0];
    const i = quaternion[1];
    const j = quaternion[2];
    const k = quaternion[3];

    const twoS = 2.0 / (quaternion[0] ** 2 + quaternion[1] ** 2 + quaternion[2] ** 2 + quaternion[3] ** 2);

    const m00 = 1 - twoS * (j * j + k * k);
    const m01 = twoS * (i * j - k * r);
    const m02 = twoS * (i * k + j * r);

    const m10 = twoS * (i * j + k * r);
    const m11 = 1 - twoS * (i * i + k * k);
    const m12 = twoS * (j * k - i * r);

    const m20 = twoS * (i * k - j * r);
    const m21 = twoS * (j * k + i * r);
    const m22 = 1 - twoS * (i * i + j * j);

    return [
        [m00, m01, m02],
        [m10, m11, m12],
        [m20, m21, m22],
    ];
}
function matrixToQuaternion(matrix) {
    if (matrix.length !== 3 || matrix[0].length !== 3) {
        throw new Error(`Invalid rotation matrix shape. Expected 3x3 matrix.`);
    }

    const m00 = matrix[0][0];
    const m01 = matrix[0][1];
    const m02 = matrix[0][2];
    const m10 = matrix[1][0];
    const m11 = matrix[1][1];
    const m12 = matrix[1][2];
    const m20 = matrix[2][0];
    const m21 = matrix[2][1];
    const m22 = matrix[2][2];

    const qAbs = [
        Math.sqrt(Math.max(0, 1.0 + m00 + m11 + m22)),
        Math.sqrt(Math.max(0, 1.0 + m00 - m11 - m22)),
        Math.sqrt(Math.max(0, 1.0 - m00 + m11 - m22)),
        Math.sqrt(Math.max(0, 1.0 - m00 - m11 + m22))
    ];
    flr = [0.1,0.1,0.1,0.1];
    const qAbsWithFloor = qAbs.map((value, index) => 2 * Math.max(value, flr[index]));
    const quat_by_rijk = [
        [qAbs[0] ** 2, m21 - m12, m02 - m20, m10 - m01],
        [m21 - m12, qAbs[1] ** 2, m10 + m01, m02 + m20],
        [m02 - m20, m10 + m01, qAbs[2] ** 2, m12 + m21],
        [m10 - m01, m20 + m02, m21 + m12, qAbs[3] ** 2]
    ];
    const quatCandidates = quat_by_rijk.map(row => { return row.map((val, colIndex) => val / (qAbsWithFloor[colIndex]))} );
    const qAbs_max = Math.max(...qAbs);
    const maxIndex = qAbs.indexOf(qAbs_max); 
    const quaternion = [quatCandidates[0][maxIndex], quatCandidates[1][maxIndex], quatCandidates[2][maxIndex], quatCandidates[3][maxIndex]];
    return standardizeQuaternion(quaternion);
}

function standardizeQuaternion(quaternion) {
    if (quaternion[0] < 0) {
        return quaternion.map(val => -val);
    }
    return quaternion;
}
function multiplyMatrices(matrixA, matrixB) {
    const result = [];
    for (let i = 0; i < 3; i++) {
        result[i] = [];
        for (let j = 0; j < 3; j++) {
            let sum = 0;
            for (let k = 0; k < 3; k++) {
                sum += matrixA[i][k] * matrixB[k][j];
            }
            result[i][j] = sum;
        }
    }
    return result;
}
function multiplyMatirx_vector(matrixA, vector) {
    let result = [0,0,0];
    for (let i = 0; i < 3; i++) {
        let sum = 0;
        for (let j = 0; j < 3; j++) {
            sum += matrixA[i][j] * vector[j];
        }
        result[i] = sum;
    }
    return result;
}
function generate_yaw_matrix(yaw) {
    const cos = Math.cos(yaw);
    const sin = Math.sin(yaw);
    return [
        [cos, 0, sin],
        [0, 1, 0],
        [-sin, 0, cos]
    ];
}

