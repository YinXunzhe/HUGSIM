function eulerToRotationMatrix(euler) {
    const [alpha, beta, gamma] = euler.map(angle => angle * Math.PI / 180);

    const Rx = [
        [1, 0, 0],
        [0, Math.cos(alpha), -Math.sin(alpha)],
        [0, Math.sin(alpha), Math.cos(alpha)]
    ];

    const Ry = [
        [Math.cos(beta), 0, Math.sin(beta)],
        [0, 1, 0],
        [-Math.sin(beta), 0, Math.cos(beta)]
    ];

    const Rz = [
        [Math.cos(gamma), -Math.sin(gamma), 0],
        [Math.sin(gamma), Math.cos(gamma), 0],
        [0, 0, 1]
    ];

    let R = multiplyMatrices(multiplyMatrices(Rx, Ry), Rz);

    return R;
}

function createTransformationMatrix(angles, translation) {
    let rotationMatrix = eulerToRotationMatrix(angles);
    return [
        rotationMatrix[0][0], rotationMatrix[0][1], rotationMatrix[0][2], translation[0],
        rotationMatrix[1][0], rotationMatrix[1][1], rotationMatrix[1][2], translation[1],
        rotationMatrix[2][0], rotationMatrix[2][1], rotationMatrix[2][2], translation[2],
        0, 0, 0, 1
    ];   
}

function matrixMultiply_column(flattenedA, flattenedB) {
    if (flattenedA.length !== 16 || flattenedB.length !== 16) {
        throw new Error("error");
    }
    const matrixA = [];
    const matrixB = [];
    for (let i = 0; i < 16; i += 4) {
        matrixA.push(flattenedA.slice(i, i + 4));
        matrixB.push(flattenedB.slice(i, i + 4));
    }
    const resultMatrix = Array.from({ length: 4 }, () => Array(4).fill(0));
    for (let i = 0; i < 4; i++) {
        for (let j = 0; j < 4; j++) {
            for (let k = 0; k < 4; k++) {
                resultMatrix[i][j] += matrixA[i][k] * matrixB[k][j];
            }
        }
    }
    const resultFlattened = resultMatrix.flat();
    return resultFlattened;
}

